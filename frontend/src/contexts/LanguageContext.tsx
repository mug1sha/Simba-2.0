const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem("simba-language");
    return saved === "RW" || saved === "FR" || saved === "EN" ? saved : "EN";
  });

  const setLanguage = (lang: Language) => {
    localStorage.setItem("simba-language", lang);
    setLanguageState(lang);
  };

  const t = (key: string, variables?: Record<string, string | number>) => {
    const template = translations[language][key] || translations.EN[key] || key;
    if (!variables) return template;

    return Object.entries(variables).reduce((result, [token, value]) => {
      return result.replaceAll(`{${token}}`, String(value));
    }, template);
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) throw new Error("useLanguage must be used within LanguageProvider");
  return context;
};
